    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        fila = this.jTable1.rowAtPoint(evt.getPoint());
        
        this.txtNombre.setText(this.model.getValueAt(fila, 0).toString());
        this.txtApellido.setText(this.model.getValueAt(fila, 1).toString());
        this.txtEdad.setText(this.model.getValueAt(fila, 2).toString());
        this.txtDeporte.setText(this.model.getValueAt(fila, 3).toString());
        this.chbSoltero.setSelected(Boolean.valueOf(this.model.getValueAt(fila, 4).toString()));
        
        this.btnModificar.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        model.setValueAt(txtNombre.getText(), fila, 0);
        model.setValueAt(txtApellido.getText(), fila, 1);
        model.setValueAt(txtEdad.getText(), fila, 2);
        model.setValueAt(txtDeporte.getText(), fila, 3);
        model.setValueAt(chbSoltero.isSelected(), fila, 4);
        
        this.btnModificar.setEnabled(false);
        
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtEdad.setText("");
        this.txtDeporte.setText("");
        this.chbSoltero.setSelected(false);
        
        this.jTable1.setModel(model);
    }//GEN-LAST:event_btnModificarActionPerformed

