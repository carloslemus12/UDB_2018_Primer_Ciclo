    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        int edad = Integer.valueOf(txtEdad.getText());
        lblDatos.setText(txtNombre.getText() + " " + txtApellido.getText() + " " + edad);
        if (edad > 17)
            JOptionPane.showMessageDialog(this, "Es mayor de edad");
        else
            JOptionPane.showMessageDialog(this, "Es menor de edad.");
    }//GEN-LAST:event_btnIngresarActionPerformed

