    private void btnCursarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCursarMateriaActionPerformed
        // TODO add your handling code here:
        int indice = this.cmbMateriasACursar.getSelectedIndex();
        
        if (!this.indicesUsados.contains(indice)) {
            String cor = this.materias.getField(indice, "Indice");
            String nombre = this.materias.getField(indice, "Nombre");

            DefaultTableModel model = (DefaultTableModel) this.tblMateriasCursadas.getModel();
            model.addRow(new Object[]{ cor, nombre });

            this.indicesUsados.add(indice);
        } else
            Mensaje.Error("La materia " + this.cmbMateriasACursar.getSelectedItem().toString() + " ya ha sido registrada", title);
    }//GEN-LAST:event_btnCursarMateriaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modal = (DefaultTableModel)this.tblMateriasCursadas.getModel();
        modal.removeRow(this.filaEliminar);
        this.indicesUsados.remove(this.indiceEliminar);
        this.btnEliminar.setEnabled(false);
    }//GEN-LAST:event_btnEliminarActionPerformed

