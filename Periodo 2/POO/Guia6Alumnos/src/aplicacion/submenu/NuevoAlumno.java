    // Obtenemos todas las materias
    public void ObtenerMaterias(){
        Conexion conexion;
        indicesUsados = new ArrayList<>();
        
        // Mientras no se haya oriducido ningun error
        if ((conexion = Conexion.establecerConexion()) != null) {
            ResultSet select = conexion.crearQuery("materia").select();
            
            this.cmbMateriasACursar.removeAllItems();
            
            try{
                while(select.next()){
                    this.cmbMateriasACursar.addItem(select.getString("Nombre"));
                }
                
                select.beforeFirst();
                materias = new Tabla(select);
                
                conexion.cerrarConexion();
                
            } catch(Exception ex){  }
            
            conexion.cerrarConexion();
            
        }
    }
    
    public void Limipiar(){
        this.txtId.setText("");
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.btnEliminar.setEnabled(false);
        DefaultTableModel model = (DefaultTableModel)this.tblMateriasCursadas.getModel();
        
        int size = model.getRowCount();
        for (int i = 0; i < size; i++)
            model.removeRow(0);
        
        this.indicesUsados.clear();
    }
    
