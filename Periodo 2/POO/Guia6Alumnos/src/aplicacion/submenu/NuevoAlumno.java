    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (Validacion.ComprobarDatosTextBox(new javax.swing.JTextField[]{ this.txtId, this.txtNombre, this.txtApellido})) {
            Conexion conexion;
            
            // Mientras no se haya oriducido ningun error
            if ((conexion = Conexion.establecerConexion()) != null) {
                
                // Informacion del usuario
                int indice = Integer.valueOf(this.txtId.getText().trim());
                String nombre = this.txtNombre.getText().trim();
                String apellido = this.txtApellido.getText().trim();
                
                
                try{
                    
                    int num = conexion.crearQuery("alumno").agregarCampoValor("Cod_alumno", indice).agregarCampoValor("Nombres", nombre).agregarCampoValor("Apellidos", apellido).insert();
                    
                    if (num > 0) {
                        
                        DefaultTableModel model = (DefaultTableModel)this.tblMateriasCursadas.getModel();
                        
                        boolean res = true;
                        
                        for (int i = 0; i < model.getRowCount(); i++) {
                            int val = Integer.valueOf(model.getValueAt(i, 0).toString());
                            
                            int id = Integer.valueOf(this.materias.getField(val, "Cod_materia"));
                            
                           int r = conexion.crearQuery("alumno_materia").agregarCampoValor("Cod_alumno", indice).agregarCampoValor("Cod_materia", id).insert();
                        
                           if (r <= 0){
                               res = false;
                               Mensaje.Error("No se ha podido guardar el alumno", title);
                               break;
                           }
                        }
                        
                        if (res){
                            Mensaje.Informacion("El usuario ha sido ingresado", title);
                            this.Limipiar();
                            this.dispose();
                        }
                        
                    } else
                        Mensaje.Error("No se ha podido guardar el alumno", title);
                    
                } catch(Exception ex){}
                
                conexion.cerrarConexion();
            } else
                Mensaje.Error("No se ha podido establecer la conexion", this.getTitle());
        } else
            Mensaje.Error("Todos los campos son obligatorios", this.getTitle());
    }//GEN-LAST:event_btnGuardarActionPerformed

